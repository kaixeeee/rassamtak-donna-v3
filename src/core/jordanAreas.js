function includesAny(text, list) {
  const n = norm(text);
  if (!n) return false;
  return list.some(x => {
    const a = norm(x);
    return a && (n.includes(a) || a.includes(n));
  });
}

function isAmmanArea(area = '') {
  return includesAny(area, AMMAN_AREAS);
}

function isNonAmmanArea(area = '') {
  const n = norm(area);
  if (!n) return false;
  if (isAmmanArea(n)) return false;
  return includesAny(n, NON_AMMAN_AREAS);
}

function normalizeDeliveryCompany(value = '') {
  const v = norm(value);
  if (/تامر/.test(v)) return 'تامر';
  if (/\bnet\b|نت/.test(v)) return 'نت';
  return String(value || '').trim();
}

function chooseDeliveryCompany({ area = '', explicitCompany = '', defaultCompany = 'نت' } = {}) {
  const explicit = normalizeDeliveryCompany(explicitCompany);
  if (explicit === 'نت' || explicit === 'تامر') return explicit;

  // قرار نهائي حسب التشغيل:
  // أي طلب بدون شركة توصيل صريحة يأخذ نت تلقائياً، سواء داخل عمّان أو خارجها أو حتى لو المنطقة غير واضحة.
  // تامر يبقى اختيار يدوي فقط عبر Reply أو رياكت 👤.
  return normalizeDeliveryCompany(defaultCompany) || 'نت';
}

function areaScope(area = '') {
  if (isAmmanArea(area)) return 'amman';
  if (isNonAmmanArea(area)) return 'outside_amman';
  return 'unknown';
}

