const { normalizeArabic, toEnglishDigits } = require('../utils/normalize');

function norm(v = '') {
  return normalizeArabic(toEnglishDigits(String(v || '')))
    .replace(/[ـ]/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

